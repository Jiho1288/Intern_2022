#과제1. 합이 1,000이고 아래 조건1,2를 만족하는 세 자연수 a,b,c의 곱을 구하시오.
#      - 조건1. a < b < c 
#      - 조건2. a^2 + b^2 = c^2 
#      - 출력값1 : a=200 / b=375 / c=425
#      - 출력값2 : 31,875,000

#과제 1 풀이
for c in range(500, 1, -1):                                         # for문을 이용해 c를 500부터 1까지 반복
    for b in range(c - 1, 1, -1):                                   # for문을 이용해 b를 c - 1부터 1까지 반복
        for a in range(b - 1, 1, -1):                               # for문을 이용해 a를 b - 1부터 1까지 반복
            if a ** 2 + b ** 2 == c ** 2 and a + b + c == 1000:     # if문을 이용해 만약 피타고라스 정의에 부합하고, a + b + c가 1000이라면,
                print(f'a={a} / b={b} / c={c}')                     # a, b, c 값 출력
                print('{:,}'.format(a * b * c))                     # a * b * b 값 출력
                break                                               # if문 break로 탈출
